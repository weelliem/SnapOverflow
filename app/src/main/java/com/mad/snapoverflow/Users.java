package com.mad.snapoverflow;

public class Users {
    public String username, email, password, university, areaOfInterest, dateOfBirth;

    public Users() {

    }

    public Users(String username, String email, String password, String university, String areaOfInterest, String dateOfBirth) {
        this.username = username;
        this.email = email;
        this.password = password;
        this.university = university;
        this.areaOfInterest = areaOfInterest;
        this.dateOfBirth = dateOfBirth;
    }
}
